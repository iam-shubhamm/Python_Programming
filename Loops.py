# while loop 

count = 0 

while count < 5: #condition 
    print(count)
    count = count + 1


# print numbers from 1 to 5 using while loop
count = 1 
while count < 6: #condition 
    print(count)
    # count = count + 1
    count += 1 


count = 5 
while count > 0: #condition 
    print(count)
    # count = count + 1
    count -= 1 
else:
    print("while loop ended")


